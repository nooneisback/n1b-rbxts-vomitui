local _t = require(script.Parent._types);
local intrinsicToClassName = {} :: {[string]:string};
local GetRobloxComponent = require(script.Parent.RobloxComponent).GetRobloxComponent;
local GetNamedComponent = require(script.Parent.Component).GetNamedComponent;

local React = {}; do
    function React.createElement(name_comp:string|_t.Component, props:_t.props, ...:_t.Element)
        local component:_t.Component;
        if (type(name_comp)~="string") then
            component = name_comp :: _t.Component;
        else
            local className = intrinsicToClassName[name_comp];
            component = className and GetRobloxComponent(className) or GetNamedComponent(name_comp) or error("Unknown component "..name_comp);
        end
        local parent = component:CreateElement(props);
        for _,child in pairs({...}) do
            child:MountTo(parent);
        end
        return parent;
    end
end

